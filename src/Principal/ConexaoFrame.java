                System.out.println("Endereço alto: "+xbm.getDevice().get64BitAddress());
                
                boolean status = xbm.isBroadcast();
                String me = status ? "Recebido broadcast: " : "Recebido unicast: ";
                dataHora = ("[" + sdf.format(c.getTime()) + "] - ");
                dataHora = me + dataHora;
